import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";
import Index from "./pages/Index";
import Admin from "./pages/Admin";
import NotFound from "./pages/NotFound";
import VehicleDetailsPage from "./pages/VehicleDetailsPage";

// Import modular Fleet pages/components
import FleetShowcasePage from "./components/FleetShowcase";
import CategoryPage from "./components/fleet/CategoryPage";

// Initialize React Query client
const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<Index />} />
          <Route path="/admin" element={<Admin />} />
          {/* Fleet routes */}
          <Route path="/fleet" element={<FleetShowcasePage />} />
          <Route path="/fleet/:category" element={<CategoryPage />} />
          <Route path="/vehicle/:vehicleId" element={<VehicleDetailsPage />} />
          {/* Catch-all */}
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
